# class Calculator
class SimpleCalculator:

    # def add
    def add(self, num1, num2):
        sum = num1 + num2
        return sum

    # def subtract
    def subtract(self, num1, num2):
        difference = num1 - num2
        return difference
    
    # def multiply
    def multiply(self, num1, num2):
        product = num1 * num2
        return product

    # def divide
    def divide(self, num1, num2):
        quotient = num1 / num2
        return quotient
